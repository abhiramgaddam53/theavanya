export const getStatusBg = (hex: string) => {
    switch (hex) {
        case "#5643C7":
            return "#EEEAFE";
        case "#0D824B":
            return "#E3F7EE";
        case "#F8C313":
            return "#FFF6D6";
        case "#12607D":
            return "#E3F3FA";
        case "#CD3636":
            return "#FDE5E5";
        default:
            return "#F3F4F6";
    }
};
